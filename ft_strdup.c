#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	size;
	char	*dest;
	int		i;

	i = 0;
	size = sizeof(char) * (ft_strlen(s) + 1);
	dest = malloc(size);
	if (dest == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
